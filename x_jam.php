        <div id="jam_patterns" class="sub_content">
            <div class="sub_content_title">JAM Patterns</div>
            <div class="sub_content_content">
                The JAM 2021 Examination for all the seven test papers will be carried out as ONLINE Computer Based Test
                (CBT) where the candidates will be shown the questions in a random sequence on a computer screen. For
                all the seven test papers, the duration of the examination will be of 3 hours. The medium for all the
                test papers will be English only. There will be a total of 60 questions carrying a total of 100 marks.
                The entire paper will be divided into three sections, A, B and C. All sections are compulsory. Questions
                in each section will be of different types as given below:
                <br><br>

                <ul>
                    <li>Section–A contains a total of 30 Multiple Choice Questions (MCQ) involving 10 questions of one
                        mark each and 20 questions of two marks each. Each MCQ has four choices out of which only one
                        choice is the correct answer. Candidates can mark the answer by clicking the choice.

                    </li>
                    <li>Section–B contains a total of 10 Multiple Select Questions (MSQ) carrying two marks each. Each
                        MSQ is similar to MCQ but with the difference that MSQ may have one or more than one correct
                        choice(s) out of the four given choices. The candidate gets full credit only if he/she selects
                        all the correct answer(s) only and no wrong answers. Candidates can mark the answer(s) by
                        clicking the choice(s).

                    </li>
                    <li>Section–C contains a total of 20 Numerical Answer Type (NAT) questions involving 10 questions of
                        one mark each and 10 questions of two marks each. For these NAT type questions, the answer is a
                        signed real number, which needs to be entered using the virtual numeric keypad on the monitor.
                        No choices will be shown for NAT questions.

                    </li>
                    <li>In all sections, questions not attempted will result in zero marks. In Section-A (MCQ), wrong
                        answer will result in negative marks. For each wrong answer to 1 mark questions, 1/3 mark will
                        be deducted, and similarly, for each wrong answer to 2 marks questions, 2/3 mark will be
                        deducted. In Section-B (MSQ), there are no negative and no partial marking provisions. There is
                        no negative marking in Section-C (NAT) as well.

                    </li>
                    <li>There is a provision for using an online virtual calculator. The candidates, therefore, should
                        not bring any calculator with them.

                    </li>
                    <li>Mobile phones and any other electronic devices are strictly prohibited inside the examination
                        hall. Charts, graph sheets, and tables are also NOT allowed inside the examination hall.

                    </li>
                    <li>A scribble pad will be provided for rough work. The candidate has to write his/her name and
                        registration number before using it. The candidate can possess only one scribble pad at any
                        point of time. A second scribble pad is given only upon returning the first one. The scribble
                        pad has to be returned at the end of the examination.

                    </li>
                    <li>The candidates are required to select the answer for MCQ and MSQ type questions using the mouse.
                        The answer for NAT questions can be entered using a virtual numeric keypad (the keyboard of the
                        computer will be disabled).

                    </li>
                    <li>
                        At the end of 3 hours, the computer will automatically end the examination. The candidates have
                        to be careful and submit their answers before the end of the examination.
                    </li>
                </ul>

            </div>
