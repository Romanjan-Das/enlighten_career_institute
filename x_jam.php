                other deserving and meritorious students at individual institutes. From the Academic Session 2004-05,
                IITs started conducting a Joint Admission Test for Masters (JAM). The objective of JAM is to provide
                admissions to M.Sc. (Two Years), Joint M.Sc.-Ph.D., M.Sc.-Ph.D. DualDegree, and other Post- Bachelor
                Degree Programmes at the IITs and Integrated Ph.D. Degree Programmes at the IISc and to consolidate
